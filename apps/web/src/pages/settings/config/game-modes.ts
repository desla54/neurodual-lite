function buildModeSectionIndex(
  categories: readonly ModeCategory[],
  knownModes: readonly GameMode[],
): Record<GameMode, GameModeSection> {
  const sectionByMode: Partial<Record<GameMode, GameModeSection>> = {};

  for (const category of categories) {
    for (const mode of category.modes) {
      const existing = sectionByMode[mode];
      if (existing) {
        throw new Error(
          `[game-modes] Mode "${mode}" is classified twice: "${existing}" and "${category.section}".`,
        );
      }
      sectionByMode[mode] = category.section;
    }
  }

  const missing = knownModes.filter((mode) => sectionByMode[mode] === undefined);
  if (missing.length > 0) {
    throw new Error(`[game-modes] Unclassified modes: ${missing.join(', ')}`);
  }

  return sectionByMode as Record<GameMode, GameModeSection>;
}

